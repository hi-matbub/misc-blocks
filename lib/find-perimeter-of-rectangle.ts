/**
 * 
 * @param width 
 * @param length 
 * @returns Number
 */

const findPerimeterOfRectangle = (width: Number, length: Number) => (Number(width) * 2) + (Number(length) * 2)

console.log(findPerimeterOfRectangle(6, 7));
console.log(findPerimeterOfRectangle(56, 23));
console.log(findPerimeterOfRectangle(34, 73));

